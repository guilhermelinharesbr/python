#O método istitle() retorna `True` se todas as palavras 
#em um texto começarem com letra maiúscula E o restante 
#da palavra for composto por letras minúsculas; caso 
#contrário, retorna False.
#Símbolos e números são ignorados.
#Também pode ser chamado aqui no Brasil com Capitalizado.
n=input('Digite algo: ')
print(n.istitle())
print(n.isspace)
